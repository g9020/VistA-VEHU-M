PASS ;
 K DIR S PSSSCORE="",DIR("?")=" "
 S DIR("?",1)=" Enter the Activity Score, 1 to 30 characters. This can be a whole number,"
 S DIR("?",2)=" or a number with a decimal."
 S DIR(0)="FO^1:40",DIR("A")="Enter "_PSSGLP_" Activity Score"
