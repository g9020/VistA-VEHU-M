VSE10962 ;report of appointments booked for 3/1/2026 and after and were booked before 3/10/2025 and has EAS number
 K ^XTMP("SDES922P")
 S ^XTMP("SDES922P",0)=$$FMADD^XLFDT(DT,30)_"^"_DT_"^SD*5.3*922"
