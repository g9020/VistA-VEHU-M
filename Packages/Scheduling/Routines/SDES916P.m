SDES916P ;ALB/MGD - SD*5.3*916 Post Init Routine ; July 01, 2025
 ;;5.3;SCHEDULING;**916**;AUG 13, 1993;Build 2
 ;;Per VHA Directive 6402, this routine should not be modified
 ;;
 Q
 ;
EN ;
 D FIND,TASK
 Q
 ;
FIND ;FIND THE IEN FOR "VS GUI NATIONAL"
 N SDECDA,SDECDA1
 D MES^XPDUTL("")
 D MES^XPDUTL("   Updating SDEC SETTINGS file (#409.98)")
 S SDECDA=0,SDECDA=$O(^SDEC(409.98,"B","VS GUI NATIONAL",SDECDA)) G:$G(SDECDA)="" NOFIND
 D VERSION   ;update GUI version number and date
 Q
 ;
VERSION ;SET THE NEW VERSION UPDATE IN SDEC SETTING FILE #409.98 TO 1.7.64
 S DA=SDECDA,DIE=409.98,DR="2///1.7.64;3///"_DT D ^DIE  ;update VS GUI NATIONAL
 K DIE,DR,DA
 S SDECDA1=0,SDECDA1=$O(^SDEC(409.98,"B","VS GUI LOCAL",SDECDA1)) Q:$G(SDECDA1)=""    ;get DA for the VS GUI LOCAL
 S DA=SDECDA1,DIE=409.98,DR="2///1.7.64;3///"_DT D ^DIE  ;update VS GUI LOCAL
 K DIE,DR,DA
 Q
 ;
NOFIND ;"VS GUI NATIONAL" NOT FOUND
 D MES^XPDUTL("   VS GUI NATIONAL not found in the SDEC SETTINGS file (#409.98)")
 Q
 ;
 ;
TASK ; Tasks off process to remove embedded control characters from the NOTE (#1) and NOTE AUDITING (#1.1) multiples
 D MES^XPDUTL("")
 D MES^XPDUTL(" SD*5.3*916 Post-Install to remove control characters from the")
 D MES^XPDUTL(" NOTE (#1) and NOTE AUDITING (#1.1) multiples in the SDEC APPOINTMENT")
 D MES^XPDUTL(" (#409.84) file.")
 D MES^XPDUTL("")
 N ZTDESC,ZTRTN,ZTIO,ZTSK,X,ZTDTH,ZTSAVE
 S ZTDESC="SD*5.3*916 Post Install Routine Task"
 D NOW^%DTC
 S ZTDTH=X,ZTIO="",ZTRTN="CLEANUPNOTES^SDES916P",ZTSAVE("*")=""
 D ^%ZTLOAD
 I $D(ZTSK) D
 . D MES^XPDUTL(" >>>Task "_ZTSK_" has been queued.")
 . D MES^XPDUTL("")
 I '$D(ZTSK) D
 . D MES^XPDUTL(" UNABLE TO QUEUE THIS JOB.")
 . D MES^XPDUTL(" Please contact the National Help Desk to report this issue.")
 Q
 ;
CLEANUPNOTES ;
 N APPTIEN,COMMIEN,COMMARRAY,COMAUDIEN,COMMENT,COMMENTS,EDITEDNOTE,FDA,FNDCC
 S APPTIEN=0
 F  S APPTIEN=$O(^SDEC(409.84,APPTIEN)) Q:'APPTIEN  I $D(^SDEC(409.84,APPTIEN,1)) D
 . ; Process NOTE (#1)
 . K COMMARRAY
 . S (COMMIEN,FNDCC)=0
 . F  S COMMIEN=$O(^SDEC(409.84,APPTIEN,1,COMMIEN)) Q:'COMMIEN  D
 . . S COMMARRAY(COMMIEN)=^SDEC(409.84,APPTIEN,1,COMMIEN,0)
 . . I COMMARRAY(COMMIEN)?.E1C.E D
 . . . S FNDCC=1
 . . . S COMMARRAY(COMMIEN)=$$CTRL^XMXUTIL1(COMMARRAY(COMMIEN))
 . Q:'FNDCC
 . D WP^DIE(409.84,APPTIEN_",",1,"","COMMARRAY")
 . ; Process NOTE AUDITING (#1.1)
 . S COMAUDIEN=0
 . F  S COMAUDIEN=$O(^SDEC(409.84,APPTIEN,"NOTEAUD",COMAUDIEN)) Q:'COMAUDIEN  D
 . . S COMMENT=$$GET1^DIQ(409.847,COMAUDIEN_","_APPTIEN_",",2)
 . . Q:COMMENT'?.E1C.E
 . . S FDA(409.847,COMAUDIEN_","_APPTIEN_",",2)=$$CTRL^XMXUTIL1(COMMENT)
 . . D UPDATE^DIE("","FDA") K FDA
 Q
